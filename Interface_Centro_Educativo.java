    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (jList1.isSelectionEmpty()) {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la lista.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {

            indiceSeleccionadoCurso = jList1.getSelectedIndex();
            Centro_Educativo aux1 = centrosEducativos.SelecCentroEduItem(indiceSeleccionadoCentroEdu);
            Curso aux2 = aux1.getCursos().SelecCursoItem(indiceSeleccionadoCurso);
            ventanaCurso = new Interface_Curso(centrosEducativos, this.indiceSeleccionadoCentroEdu, this.indiceSeleccionadoCurso);
            ventanaCurso.getLabel1().setText(aux2.getNombre());
            ventanaCurso.getLabel2().setText("" + aux2.getId());
            ventanaCurso.getLabel5().setText("" + f.format(aux2.getPromedio()));

            ventanaCurso.setVisible(true);

            this.dispose();
        }
    }//GEN-LAST:event_jButton5ActionPerformed

