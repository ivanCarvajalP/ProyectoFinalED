    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        indiceSeleccionadoCurso = jList1.getSelectedIndex();

        if (indiceSeleccionadoCurso != -1) {
            Centro_Educativo aux1 = centrosEducativos.SelecCentroEduItem(indiceSeleccionadoCentroEdu);

            int respuesta = (JOptionPane.showConfirmDialog(null, "¿Está seguro que quieres ELIMINAR el Curso?"));
            if (respuesta == 0) {

                aux1.getCursos().EliminarCurso(indiceSeleccionadoCurso);
                aux1.setPromedio(aux1.getCursos().promedioCurso());
                label4.setText("" + f.format(aux1.getPromedio()));
                MontarCola();
                JOptionPane.showMessageDialog(null, "El Curso se ha eliminado CORRECTAMENTE.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la lista", "Error", JOptionPane.ERROR_MESSAGE);

        }
        Centro_Educativo aux1 = centrosEducativos.SelecCentroEduItem(indiceSeleccionadoCentroEdu);
        System.out.println(aux1.getCursos().Mostrar_Cola());

    }//GEN-LAST:event_jButton3ActionPerformed

