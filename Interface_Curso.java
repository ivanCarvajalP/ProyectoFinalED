    public void MontarPila() {

        modelo = new DefaultListModel();
        Centro_Educativo aux1 = centrosEducativos.SelecCentroEduItem(indiceSeleccionadoCentroEdu);
        Curso aux2 = aux1.getCursos().SelecCursoItem(indiceSeleccionadoCurso);
        String elementos[] = aux2.getEstudiantes().Mostrar_Pila().split("\n");
        if (elementos == null) {
            modelo.addElement("Vacia");
        }
        for (int i = 0; i < elementos.length; i++) {
            modelo.addElement(elementos[i]);
            if (elementos[0].equalsIgnoreCase("Vacia")) {
                jButton2.setEnabled(false);
                jButton3.setEnabled(false);
                jButton5.setEnabled(false);
            } else {
                jButton2.setEnabled(true);
                jButton3.setEnabled(true);
                jButton5.setEnabled(true);
            }
        }
        jList1.setModel(modelo);

        String mejorEstudiante[] = aux2.getEstudiantes().BuscarMejorPromedio();
        double nota = Double.parseDouble(mejorEstudiante[1]);
        label9.setText(mejorEstudiante[0]);
        label8.setText(f.format(nota));

    }

