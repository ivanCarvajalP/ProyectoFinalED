    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        indiceSeleccionadoEstudiante = jList1.getSelectedIndex();

        if (indiceSeleccionadoEstudiante != -1) {

            Centro_Educativo aux1 = centrosEducativos.SelecCentroEduItem(indiceSeleccionadoCentroEdu);
            Curso aux2 = aux1.getCursos().SelecCursoItem(indiceSeleccionadoCurso);
            Estudiante aux3 = aux2.getEstudiantes().SelecEstudiantexItem(indiceSeleccionadoEstudiante);

            String IDGenero = "";
            if (aux3.getGenero().equalsIgnoreCase("Masculino")) {
                IDGenero = "el";
            } else if (aux3.getGenero().equalsIgnoreCase("Femenino")) {
                IDGenero = "la";

            }
            int respuesta = (JOptionPane.showConfirmDialog(null, "¿Está seguro que quieres ELIMINAR " + IDGenero + " Estudiante " + aux3.getNombre() + "?"));
            if (respuesta == 0) {
                aux2.getEstudiantes().EliminarEstudiantexItem(indiceSeleccionadoEstudiante);
                aux2.setPromedio(aux2.getEstudiantes().promedioEstu());
                label5.setText("" + f.format(aux2.getPromedio()));
//            modelo.removeElementAt(indiceSeleccionadoEstudiante);
                MontarPila();

                if (IDGenero.equalsIgnoreCase("el")) {
                    JOptionPane.showMessageDialog(null, "El Estudiante se ha eliminado CORRECTAMENTE.");

                } else if (IDGenero.equalsIgnoreCase("la")) {
                    JOptionPane.showMessageDialog(null, "La Estudiante se ha eliminado CORRECTAMENTE.");

                }

            }

        } else {

            JOptionPane.showMessageDialog(null, "Debe seleccionar un elemento de la lista", "Error", JOptionPane.ERROR_MESSAGE);
        }

        System.out.println(centrosEducativos.MostrarPU());


    }//GEN-LAST:event_jButton3ActionPerformed

